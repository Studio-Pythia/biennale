    existing_ids = {p["id"] for p in existing}
    added = 0
    skipped = 0
    for p in NEW_PAVILIONS:
        if p["id"] in existing_ids:
            skipped += 1
            continue
        existing.append(p)
        added += 1
    # Sort: Giardini first, then Arsenale, then off-site; alphabetical within
    venue_order = {"Giardini": 0, "Arsenale": 1, "off-site": 2}
    existing.sort(key=lambda p: (venue_order.get(p.get("venue", ""), 9), p.get("country", "")))
    DATA_FILE.write_text(json.dumps(existing, indent=2, ensure_ascii=False))
    print(f"Added {added} pavilions, skipped {skipped} duplicates. Total now: {len(existing)}.")


